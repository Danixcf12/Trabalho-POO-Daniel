// Enumeration that defines possible mission types
public enum MissionType {

    // Mission types available
    RESCUE,         // Rescue mission, typically aimed at saving people or resources
    PATROL,         // Patrol mission, typically involves monitoring and securing an area
    RECONNAISSANCE, // Reconnaissance mission, focused on gathering intelligence or surveying an area
    COMBAT          // Combat mission, involving direct engagement in a hostile environment

}
