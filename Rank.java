package POO;

// Enumeration representing possible military ranks for crew members
public enum Rank {
    SERGEANT,   // Sergeant – A non-commissioned officer rank
    MAJOR,      // Major – A rank above captain, typically in charge of larger units
    LIEUTENANT, // Lieutenant – A junior officer rank, usually commanding a platoon
    CORONEL,    // Colonel – A senior officer rank, typically commanding a regiment or brigade
    CAPTAIN,    // Captain – A mid-ranking officer, often in charge of a company
    SOLDIER,    // Soldier – A rank for enlisted personnel, typically performing general duties
    GENERAL     // General – The highest military rank, often responsible for overall strategic decisions
}
